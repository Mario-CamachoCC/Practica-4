# Leer datos e imprimirlos (Dos métodos)
# Creamos una variable "nombre", y la leemos después de imprimir "¿Cuál es tu nombre?"
nombre = input("¿Cuál es tu nombre? ")
# Imprimimos el nombre con el método siguiente
print(f"Hola {nombre}")
# Ahora imprimimos el nombre con este otro método
print("\nHola " + nombre)